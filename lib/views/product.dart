class Product {
  final String id;
  final String productName;
  final int productCode;
  final int unitPrice;
  final int qty;
  final int totalPrice;
  final String img;

  Product({
    required this.id,
    required this.productName,
    required this.productCode,
    required this.unitPrice,
    required this.qty,
    required this.totalPrice,
    required this.img,
  });
}